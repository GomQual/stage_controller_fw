/**
  * @brief This function handles System tick timer.
  */
void SysTick_Handler(void)
{
  /* USER CODE BEGIN SysTick_IRQn 0 */
  static int count = 0;
  /* USER CODE END SysTick_IRQn 0 */
  HAL_IncTick();
  /* USER CODE BEGIN SysTick_IRQn 1 */
  SysVar.msecFlag = 1;
  if(SysVar.msecCount) SysVar.msecCount--;

  if(count % 10 == 0)
  {
	  SysVar.csecFlag = 1;
	  if(SysVar.csecCount) SysVar.csecCount--;
  }

  if(count % 100 == 0)
  {
  	  SysVar.dsecFlag = 1;
  	  if(SysVar.dsecCount) SysVar.dsecCount--;
  }

  if(count == 0)
  {
	  SysVar.secFlag = 1;
	  if(SysVar.secCount) SysVar.secCount--;
  }

  count = (count+1)%1000;
  /* USER CODE END SysTick_IRQn 1 */
}

